##### Basics Type

#Integers
a_int, b_int = 20, 30

print("a/b:", a_int / b_int) 
print("a//b:", a_int // b_int)
print("mod(a,b):", a_int % b_int)

print("2^3:", 2 ** 3)

# Floating 
a_float, b_float = 20.5, 30.5
print("a_float // b_float:", a_float // b_float)


#Mix 
print("a_int * b_float:", a_int * b_float )


